#include <unistd.h>
#include <stdio.h>

void	ft_is_negative(int n)
{
	char	num;

	if (n >= 0)
	{
		num = 'P';
		write(1, &num, 1);
	}
	else
	{
		num = 'N';
		write(1, &num, 1);
	}
}
