#include <stdio.h>

int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = nb;
	j = 1;
	if (nb == 1)
		return (nb);
	while (i > j)
	{
		i = (i + j) / 2;
		j = nb / i;
		if (i * i == nb)
			return (i);
	}
	return (0);
}
